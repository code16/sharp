<template>
    <component
        :is="filterComp"
        :value="value"
        v-bind="filter"
        @input="handleInput"
    />
</template>

<script>
    import { filterByType } from "./filters";

    export default {
        name: 'SharpFilter',

        props: {
            filter: {
                type: Object,
                required: true,
            },
            value: [Object, Array, String, Number],
        },

        computed: {
            filterComp() {
                return filterByType(this.filter.type);
            },
        },

        methods: {
            handleInput(value) {
                this.$emit('input', value);
            },
        }
    }
</script>